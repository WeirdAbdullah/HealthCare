package com.example.healthcare.model;

public class DoctorModel {
    public int img;
    public String degree, name, special, rate, adress, patience, Experience, Bio;

    public DoctorModel(int img, String degree, String name, String special, String rate, String adress, String patience, String Experience, String Bio){
        this.name = name;
        this.img = img;
        this.degree = degree;
        this.special = special;
        this.rate = rate;
        this.adress = adress;
        this. patience = patience;
        this.Experience = Experience;
        this.Bio = Bio;

    }
}
